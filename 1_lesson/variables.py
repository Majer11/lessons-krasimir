"""
A variable name must start with a letter or underscore
A variable name must contain only alpha-numeric chars and underscores
Variable names are case-sensitive
"""

# Integer
# whole number
# variable name = value
number = 5
number = 10
print(f"Integer: {number}. Type: {type(number)}")

# Float
# non-whole number
number2 = 5.5434
print(f"Float: {number2}. Type: {type(number2)}")

# String
word = "Hello, World!1"
print(f"String: {word}. Type: {type(word)}")

# Boolean
# Only two values - True or False
boolean_variable = True
print(f"Boolean: {boolean_variable}. Type: {type(boolean_variable)}")

